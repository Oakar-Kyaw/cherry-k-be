'use strict';
const AccountHeader = require('../models/accountHeader');

exports.listAllAccountHeaders = async (req, res) => {
  let { keyword, role, limit, skip } = req.query;
  let count = 0;
  let page = 0;
  try {
    limit = +limit <= 100 ? +limit : 20; //limit
    skip = +skip || 0;
    let query = {isDeleted:false},
      regexKeyword;
    role ? (query['role'] = role.toUpperCase()) : '';
    keyword && /\w/.test(keyword)
      ? (regexKeyword = new RegExp(keyword, 'i'))
      : '';
    regexKeyword ? (query['name'] = regexKeyword) : '';
    let result = await AccountHeader.find(query).populate('relatedAccountType')
    count = await AccountHeader.find(query).count();
    const division = count / limit;
    page = Math.ceil(division);

    res.status(200).send({
      success: true,
      count: count,
      _metadata: {
        current_page: skip / limit + 1,
        per_page: limit,
        page_count: page,
        total_count: count,
      },
      list: result,
    });
  } catch (e) {
    return res.status(500).send({ error: true, message: e.message });
  }
};

exports.getAccountHeader = async (req, res) => {
  const result = await AccountHeader.find({ _id: req.params.id,isDeleted:false }).populate('relatedAccountType');
  if (!result)
    return res.status(500).json({ error: true, message: 'No Record Found' });
  return res.status(200).send({ success: true, data: result });
};

exports.getRelatedAccountHeader = async (req, res) => {
  const result = await AccountHeader.find({ relatedAccountType: req.params.id,isDeleted:false }).populate('relatedAccountType');
  if (!result)
    return res.status(500).json({ error: true, message: 'No Record Found' });
  return res.status(200).send({ success: true, data: result });
};

exports.createAccountHeader = async (req, res, next) => {
  try {
    const newBody = req.body;
    const newAccountHeader = new AccountHeader(newBody);
    const result = await newAccountHeader.save();
    res.status(200).send({
      message: 'AccountHeader create success',
      success: true,
      data: result
    });
  } catch (error) {
    return res.status(500).send({ "error": true, message: error.message })
  }
};

exports.updateAccountHeader = async (req, res, next) => {
  try {
    const result = await AccountHeader.findOneAndUpdate(
      { _id: req.body.id },
      req.body,
      { new: true },
    ).populate('relatedAccountType');
    return res.status(200).send({ success: true, data: result });
  } catch (error) {
    return res.status(500).send({ "error": true, "message": error.message })
  }
};

exports.deleteAccountHeader = async (req, res, next) => {
  try {
    const result = await AccountHeader.findOneAndUpdate(
      { _id: req.params.id },
      { isDeleted: true },
      { new: true },
    );
    return res.status(200).send({ success: true, data: { isDeleted: result.isDeleted } });
  } catch (error) {
    return res.status(500).send({ "error": true, "message": error.message })

  }
}

